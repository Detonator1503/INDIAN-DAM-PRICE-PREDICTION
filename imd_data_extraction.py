import os
import glob
import struct
import numpy as np
import pandas as pd

# 1. GEOGRAPHICAL TARGET LOCATIONS
locations = {
    "Delhi":     {"lat": 28.61, "lon": 77.20},
    "Mumbai":    {"lat": 19.07, "lon": 72.87},
    "Bengaluru": {"lat": 12.97, "lon": 77.59},
    "Kolkata":   {"lat": 22.57, "lon": 88.36},
    "Chennai":   {"lat": 13.08, "lon": 80.27}
}

# 2. CONSTRUCT EXACT IMD COORDINATE ARRAYS
# Rainfall Layout: 0.25 x 0.25 grid (Flipped Lats: 38.5 down to 6.5)
rain_lats = np.arange(38.5, 6.25, -0.25)
rain_lons = np.arange(66.5, 100.25, 0.25)

# TMAX Layout: 1.0 x 1.0 grid (Flipped Lats: 37.5 down to 7.5)
tmax_lats = np.arange(37.5, 6.5, -1.0)
tmax_lons = np.arange(67.5, 98.5, 1.0)

# TMIN Layout: 1.0 x 1.0 grid (Flipped Lats: 38.5 down to 6.5)
tmin_lats = np.arange(38.5, 5.5, -1.0)
tmin_lons = np.arange(66.5, 101.5, 1.0)

# 3. DIRECTORY ASSIGNMENT
dir_max  = "/content/drive/MyDrive/LJMU/DATASET/IMD_TEMP_MAX"
dir_min  = "/content/drive/MyDrive/LJMU/DATASET/IMD_TEMP_MIN"
dir_rain = "/content/drive/MyDrive/LJMU/DATASET/IMD_RAIN"

files_max  = glob.glob(os.path.join(dir_max, "tmax_*.grd")) + glob.glob(os.path.join(dir_max, "tmax_*.GRD"))
files_min  = glob.glob(os.path.join(dir_min, "tmin_*.grd")) + glob.glob(os.path.join(dir_min, "tmin_*.GRD"))
files_rain = glob.glob(os.path.join(dir_rain, "rain_*.grd")) + glob.glob(os.path.join(dir_rain, "rain_*.GRD"))

def extract_dates(file_list, prefix):
    dates = set()
    for f in file_list:
        d_str = os.path.basename(f).lower().replace(prefix, "").replace(".grd", "")
        dates.add(d_str)
    return dates

all_dates = sorted(list(extract_dates(files_max, "tmax_").union(extract_dates(files_min, "tmin_")).union(extract_dates(files_rain, "rain_"))))
master_records = []

# 4. COORDINATE SEARCH & FALLBACK ALGORITHM
def query_spatial_grid(matrix, target_lat, target_lon, lat_array, lon_array, max_radius=3):
    if matrix is None:
        return None

    # Standard land temperature filtering boundaries
    MIN_VAL, MAX_VAL = -10.0, 55.0

    # Step 1: Find the absolute closest grid indices mathematically
    base_lat_idx = np.abs(lat_array - target_lat).argmin()
    base_lon_idx = np.abs(lon_array - target_lon).argmin()

    # Step 2: Progressive square expansion search to clear coastal/missing masks
    for r in range(max_radius + 1):
        for r_idx in range(base_lat_idx - r, base_lat_idx + r + 1):
            for c_idx in range(base_lon_idx - r, base_lon_idx + r + 1):
                if 0 <= r_idx < len(lat_array) and 0 <= c_idx < len(lon_array):
                    val = matrix[r_idx, c_idx]
                    # Filter out NaN or standard IMD missing values
                    if val is not None and not np.isnan(val) and MIN_VAL <= val <= MAX_VAL:
                        return val # Immediate return of nearest real weather data cell
    return None

# 5. CORE PROCESSOR LOOP
for date_str in all_dates:
    row_dict = {"Date": date_str}

    def load_binary(folder, prefix, expected_len):
        for ext in ['.grd', '.GRD']:
            f_path = os.path.join(folder, f"{prefix}{date_str}{ext}")
            if os.path.exists(f_path):
                try:
                    with open(f_path, 'rb') as f:
                        b_data = f.read()
                        raw_vals = struct.unpack(f'{expected_len}f', b_data[:expected_len*4])
                        return np.array(raw_vals)
                except Exception:
                    pass
        return None

    # Load and reshape to standard (Lats, Lons) dimensional specifications
    arr_max  = load_binary(dir_max, "tmax_", len(tmax_lats) * len(tmax_lons))
    matrix_max = arr_max.reshape(len(tmax_lats), len(tmax_lons)) if arr_max is not None else None

    arr_min  = load_binary(dir_min, "tmin_", len(tmin_lats) * len(tmin_lons))
    matrix_min = arr_min.reshape(len(tmin_lats), len(tmin_lons)) if arr_min is not None else None

    arr_rain = load_binary(dir_rain, "rain_", len(rain_lats) * len(rain_lons))
    matrix_rain = arr_rain.reshape(len(rain_lats), len(rain_lons)) if arr_rain is not None else None

    # 6. EXTRACT FOR CITIES USING SPATIAL LOOKUP
    for loc_name, coords in locations.items():
        lat, lon = coords["lat"], coords["lon"]

        # Max Temp Extract
        tmax_val = query_spatial_grid(matrix_max, lat, lon, tmax_lats, tmax_lons)
        row_dict[f"{loc_name}_Tmax_C"] = round(float(tmax_val), 2) if tmax_val is not None else None

        # Min Temp Extract
        tmin_val = query_spatial_grid(matrix_min, lat, lon, tmin_lats, tmin_lons)
        row_dict[f"{loc_name}_Tmin_C"] = round(float(tmin_val), 2) if tmin_val is not None else None

        # Rainfall Extract (Using high-res rain grid limits)
        if matrix_rain is not None:
            r_lat_idx = np.abs(rain_lats - lat).argmin()
            r_lon_idx = np.abs(rain_lons - lon).argmin()
            rv = matrix_rain[r_lat_idx, r_lon_idx]
            row_dict[f"{loc_name}_Rainfall_mm"] = round(float(rv), 2) if (rv >= 0 and rv < 500) else 0.0
        else:
            row_dict[f"{loc_name}_Rainfall_mm"] = None

    master_records.append(row_dict)
    print(f"Processed row entries for timeline date: {date_str}")

# 7. EXCEL EXPORT
if master_records:
    df = pd.DataFrame(master_records)
    df.to_csv("/content/drive/MyDrive/LJMU/DATASET/comprehensive_weather_data.csv", index=False)
    print("\n🎉 Complete! The matrix has been successfully compiled into 'comprehensive_weather_data.xlsx'.")

